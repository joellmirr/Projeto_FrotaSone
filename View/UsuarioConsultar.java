  public void limpar(){
        
        //Limpa a tabela
        DefaultTableModel model = (DefaultTableModel)jTableUsuario.getModel(); 
        model.setNumRows(0);
        
        //Limpando os campos de texto
        jTID.setText("");
        jTLOGIN.setText("");
        jTNOME.setText("");
        jTSENHA.setText("");
        jTPERFIL.setText("");
        jTSTATUS.setText("");
        
        
        //Colocando o foco na text do nome artístico
        jTLoginBuscar.grabFocus();
      }
      
     
    
   
   public void carregarTabela(){
        Usuario usuario = new Usuario();
        ArrayList<Usuario> arrayDeUsuarios = new ArrayList<Usuario>(); 
        
        //Chamando a busca no banco passando a data
        arrayDeUsuarios = usuario.buscar(jTLoginBuscar.getText());
        
        //Apagando os dados da tabela para mostrar os novos
        limpar();
        
        DefaultTableModel model = (DefaultTableModel) jTableUsuario.getModel();
        
        //Andando na lista de tarefa e montando as linhas do TABLEMODEL
        for(int i=0; i<arrayDeUsuarios.size(); i++){
            
            //Criando as colunas da linha i do TABLEMODEL
            String colunas[] = new String[6];
            colunas[0] = String.valueOf(arrayDeUsuarios.get(i).getCodigo());
            colunas[1] = String.valueOf(arrayDeUsuarios.get(i).getLogin());
            colunas[2] = String.valueOf(arrayDeUsuarios.get(i).getNome());
            colunas[3] = String.valueOf(arrayDeUsuarios.get(i).getSenha());
            colunas[4] = String.valueOf(arrayDeUsuarios.get(i).getPerfil());
            colunas[5] = String.valueOf(arrayDeUsuarios.get(i).getStatus());
            
            
            
            
            //Adicionando uma linha no TABLEMODEL
            model.addRow(colunas);
        } 

        jTableUsuario.setModel(model);
    }    
    
    
