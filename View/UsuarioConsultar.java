    private void jBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAlterarActionPerformed
        Usuario objUsuario = new Usuario();
     
        objUsuario.setCodigo(jTID.getText());
        objUsuario.setLogin(jTLOGIN.getText());
        objUsuario.setNome(jTNOME.getText());
        objUsuario.setSenha(jTSENHA.getText());
        objUsuario.setPerfil(jTPERFIL.getText());
        objUsuario.setStatus(jTSTATUS.getText());
        objUsuario.alterar();

        jTLoginBuscar.setText(objUsuario.getLogin());
        carregarTabela();
    }//GEN-LAST:event_jBAlterarActionPerformed

