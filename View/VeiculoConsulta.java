   public void carregarTabela(){
        Veiculo veiculo = new Veiculo();
        ArrayList<Veiculo> arrayDeVeiculos = new ArrayList<Veiculo>(); 
        
        //Chamando a busca no banco passando a data
        arrayDeVeiculos = veiculo.buscar(jTPlacaBusca.getText());
        
        //Apagando os dados da tabela para mostrar os novos
        limpar();
        
        DefaultTableModel model = (DefaultTableModel) jTableVEICULO.getModel();
        
        //Andando na lista de tarefa e montando as linhas do TABLEMODEL
        for(int i=0; i<arrayDeVeiculos.size(); i++){
            
            //Criando as colunas da linha i do TABLEMODEL
            String colunas[] = new String[11];
            colunas[0] = String.valueOf(arrayDeVeiculos.get(i).getCodigo());
            colunas[1] = String.valueOf(arrayDeVeiculos.get(i).getMarca());
            colunas[2] = String.valueOf(arrayDeVeiculos.get(i).getModelo());
            colunas[3] = String.valueOf(arrayDeVeiculos.get(i).getPlaca());
            colunas[4] = String.valueOf(arrayDeVeiculos.get(i).getAno_fab_mod());
            colunas[5] = String.valueOf(arrayDeVeiculos.get(i).getCidade_estado());
            colunas[6] = String.valueOf(arrayDeVeiculos.get(i).getSeguro());
            colunas[7] = String.valueOf(arrayDeVeiculos.get(i).getInicio_seguro());
            colunas[8] = String.valueOf(arrayDeVeiculos.get(i).getVencimento_seguro());
            colunas[9] = String.valueOf(arrayDeVeiculos.get(i).getSeguradora());
            colunas[10] = String.valueOf(arrayDeVeiculos.get(i).getStatus());
            
            
            
            //Adicionando uma linha no TABLEMODEL
            model.addRow(colunas);
        } 

        jTableVEICULO.setModel(model);
    }
    
    

