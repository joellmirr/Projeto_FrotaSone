   public void carregarTabela(){
        Viagem viagem = new Viagem();
        ArrayList<Viagem> arrayDeViagem = new ArrayList<Viagem>(); 
        
        //Chamando a busca no banco passando a data
        arrayDeViagem = viagem.buscar(jTViagemBuscar.getText());
        
        //Apagando os dados da tabela para mostrar os novos
        limpar();
        
        DefaultTableModel model = (DefaultTableModel) jTableVIAGEM.getModel();
        
        //Andando na lista de tarefa e montando as linhas do TABLEMODEL
        for(int i=0; i<arrayDeViagem.size(); i++){
            
            //Criando as colunas da linha i do TABLEMODEL
            String colunas[] = new String[10];
            colunas[0] = String.valueOf(arrayDeViagem.get(i).getIdviagem());
            colunas[1] = String.valueOf(arrayDeViagem.get(i).getMotorista());
            colunas[2] = String.valueOf(arrayDeViagem.get(i).getCpf());
            colunas[3] = String.valueOf(arrayDeViagem.get(i).getOrigem());
            colunas[4] = String.valueOf(arrayDeViagem.get(i).getDestino());
            colunas[5] = String.valueOf(arrayDeViagem.get(i).getPlacaveiculo());
            colunas[6] = String.valueOf(arrayDeViagem.get(i).getDataida());
            colunas[7] = String.valueOf(arrayDeViagem.get(i).getDatavolta());
            colunas[8] = String.valueOf(arrayDeViagem.get(i).getDetalhesviagem());
            colunas[9] = String.valueOf(arrayDeViagem.get(i).getStatus());
            
            
            
            //Adicionando uma linha no TABLEMODEL
            model.addRow(colunas);
        } 

        jTableVIAGEM.setModel(model);
    }
    
    
